def get_input_and_add():
    # Get input from the user
    number1 = float(input("Enter the first number: "))
    number2 = float(input("Enter the second number: "))

    # Add the two numbers
    result = number1 + number2

    # Print the result
    print("The sum of the two numbers is:", result)

# Run the function
get_input_and_add()
